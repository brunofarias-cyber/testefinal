export const getYearOptions = () => Object.keys(allBnccCodes);

export const getCodesByYear = (year) => allBnccCodes[year] || [];

export const getCodeDescription = (code) => {
    for (const year in allBnccCodes) {
        const found = allBnccCodes[year].find(c => c.code === code);
        if (found) return found;
    }
    return null;
};

// Sugestões de IA baseadas em temas e subdomínios
export const getAISuggestions = (projectTitle, projectDescription) => {
    const keywords = `${projectTitle} ${projectDescription}`.toLowerCase();
    
    const suggestions = [];
    
    for (const year in allBnccCodes) {
        allBnccCodes[year].forEach(comp => {
            const themeMatch = comp.theme.toLowerCase().includes(
                keywords.split(' ').filter(w => w.length > 3)[0]
            );
            const descMatch = comp.description.toLowerCase().includes(
                keywords.split(' ').filter(w => w.length > 4)[0]
            );
            
            if (themeMatch || descMatch) {
                suggestions.push({
                    code: comp.code,
                    description: comp.description,
                    theme: comp.theme,
                    year: year,
                    relevance: (themeMatch ? 2 : 0) + (descMatch ? 1 : 0)
                });
            }
        });
    }
    
    return suggestions.sort((a, b) => b.relevance - a.relevance).slice(0, 5);
};
